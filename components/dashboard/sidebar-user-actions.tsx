interface SidebarUserActionsProps {
  onNavigate?: (href: string) => void;
}

export function SidebarUserActions({ onNavigate }: SidebarUserActionsProps) {
  const router = useRouter();
  const [avatarUrl, setAvatarUrl] = useState<string | null>(null);

  useEffect(() => {
    const supabase = createClient();
    supabase.auth.getUser().then(({ data }) => {
      const url = data.user?.user_metadata?.avatar_url as string | undefined;
      if (url) setAvatarUrl(url);
    });
  }, []);

  const go = (href: string) => {
    onNavigate?.(href);
    router.push(href);
  };

  const btnStyle: React.CSSProperties = {
    background: "transparent",
    border: "none",
    display: "flex",
    alignItems: "center",
    justifyContent: "center",
    cursor: "pointer",
    padding: 0,
    flexShrink: 0,
  };

  return (
    <div style={{ display: "flex", alignItems: "center", gap: 18.5 }}>
      <button
        type="button"
        onClick={() => go("/profile")}
        aria-label="Profile"
        style={btnStyle}
      >
        {avatarUrl ? (
          <Image
            src={avatarUrl}
            alt="Profile"
            width={22}
            height={22}
            style={{ borderRadius: "50%", display: "block" }}
            unoptimized
          />
        ) : (
          <IcProfile />
        )}
      </button>

      <button
        type="button"
        onClick={() => go("/settings")}
        aria-label="Settings"
        style={btnStyle}
      >
        <IcSettings />
      </button>

      <button
        type="button"
        onClick={() => go("/notifications")}
        aria-label="Notifications"
        style={btnStyle}
      >
        <IcBell />
      </button>
    </div>
  );
}
